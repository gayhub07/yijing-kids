export interface GuaLine {
  text: string // 爻辞原文
  pinyin: string // 爻辞拼音
  meaning: string // 儿童版解释
}

export interface GuaComplete {
  id: number // 卦序号
  name: string // 卦名
  namePinyin: string // 卦名拼音
  symbol: string // Unicode 卦象符号
  upperGua: string // 上卦
  lowerGua: string // 下卦
  text: string // 卦辞原文
  textPinyin: string // 卦辞拼音
  textMeaning: string // 卦辞儿童解释
  lines: GuaLine[] // 六爻
  image: string // 象传
  imagePinyin: string // 象传拼音
  story: {
    title: string
    summary: string
    content: string[]
    emoji: string
    level: 'beginner' | 'explorer' | 'advanced'
    lesson: string
  }
  application: string // 生活应用场景
}

