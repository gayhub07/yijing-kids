// 获取所有卦的简单列表（用于故事列表）
export function getAllGuaList() {
  return guaCompleteData.map((gua) => ({
    id: gua.id,
    name: gua.name,
    namePinyin: gua.namePinyin,
    symbol: gua.symbol,
    title: gua.story.title,
    summary: gua.story.summary,
    emoji: gua.story.emoji,
    level: gua.story.level,
  }))
}

// 根据ID获取完整卦信息
export function getGuaById(id: number): GuaComplete | undefined {
  return guaCompleteData.find((gua) => gua.id === id)
}

// 根据名称获取完整卦信息
export function getGuaByName(name: string): GuaComplete | undefined {
  return guaCompleteData.find((gua) => gua.name === name)
}
